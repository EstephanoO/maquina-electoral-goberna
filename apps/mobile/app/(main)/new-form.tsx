const ACCESSORY_ID = 'goberna-form-toolbar';

function KeyboardToolbar({
  onPrev,
  onNext,
  onDone,
  hasPrev,
  hasNext,
  primaryColor,
}: {
  onPrev: () => void;
  onNext: () => void;
  onDone: () => void;
  hasPrev: boolean;
  hasNext: boolean;
  primaryColor: string;
}) {
  if (Platform.OS !== 'ios') return null;
  return (
    <InputAccessoryView nativeID={ACCESSORY_ID}>
      <View style={styles.toolbar}>
        <View style={styles.toolbarNav}>
          <Pressable
            onPress={onPrev}
            disabled={!hasPrev}
            style={[styles.toolbarBtn, !hasPrev && styles.toolbarBtnDisabled]}
            accessibilityLabel="Campo anterior"
            accessibilityRole="button"
          >
            <Text style={[styles.toolbarBtnText, !hasPrev && styles.toolbarBtnTextDisabled]}>◀</Text>
          </Pressable>
          <Pressable
            onPress={onNext}
            disabled={!hasNext}
            style={[styles.toolbarBtn, !hasNext && styles.toolbarBtnDisabled]}
            accessibilityLabel="Siguiente campo"
            accessibilityRole="button"
          >
            <Text style={[styles.toolbarBtnText, !hasNext && styles.toolbarBtnTextDisabled]}>▶</Text>
          </Pressable>
        </View>
        <Pressable
          onPress={onDone}
          style={styles.toolbarDoneBtn}
          accessibilityLabel="Cerrar teclado"
          accessibilityRole="button"
        >
          <Text style={[styles.toolbarDoneText, { color: primaryColor }]}>Listo</Text>
        </Pressable>
      </View>
    </InputAccessoryView>
  );
}

export default function NewFormScreen() {
  const router = useRouter();
  const params = useLocalSearchParams<{ prefill?: string }>();
  const candidate = useCandidate();
  const formConfig = useFormConfig();
  const agent = useAgent();
  const campaign = useActiveCampaign();

  const primary = candidate.color_primario;
  const secondary = candidate.color_secundario;

  // ── Prefill: reverse-map backend field names → form field IDs ──
  const initialFormData = useMemo(() => {
    if (!params.prefill || !formConfig) return {};
    try {
      const parsed = JSON.parse(params.prefill);
      const data: Record<string, unknown> = parsed.data ?? parsed;
      // Build reverse map: backendFieldName → formFieldId
      const reverseMap: Record<string, string> = {};
      for (const field of formConfig.schema.fields) {
        const backendKey = resolveBackendField(field);
        if (backendKey && !reverseMap[backendKey]) {
          reverseMap[backendKey] = field.id;
        }
      }
      // Populate formData using reverse map
      const result: Record<string, string> = {};
      for (const [key, value] of Object.entries(data)) {
        const fieldId = reverseMap[key];
        if (fieldId && typeof value === 'string' && value.trim()) {
          result[fieldId] = value;
        }
      }
      return result;
    } catch {
      return {};
    }
  }, [params.prefill, formConfig]);

  const [formData, setFormData] = useState<Record<string, string>>({});
  const [ubicacionUtm, setUbicacionUtm] = useState<UtmData | null>(null);
  const [lugarRegistro, setLugarRegistro] = useState<SelectedDistrito | null>(null);
  const [enviando, setEnviando] = useState(false);

  // Seed formData from prefill once formConfig is available
  useEffect(() => {
    if (Object.keys(initialFormData).length > 0) {
      setFormData(initialFormData);
    }
  }, [initialFormData]);

  // GPS es opcional cuando el agente selecciona un distrito (dato capturado en papel)
  const gpsOpcional = lugarRegistro !== null;

  // ── Scroll-to-focused-field infrastructure ───────────────────
  const scrollRef = useRef<ScrollView>(null);
  const fieldLayoutsRef = useRef<Record<string, number>>({}); // fieldId → y offset
  const inputRefsRef = useRef<Record<string, TextInput | null>>({});
  const [focusedIdx, setFocusedIdx] = useState(-1);

  // Build ordered list of text-input field IDs (for prev/next navigation)
  const textFieldIds = useMemo(() => {
    if (!formConfig) return [];
    const ids: string[] = [];
    for (const f of formConfig.schema.fields) {
      // Only fields with TextInput (text, phone, number, email, textarea)
      if (f.type !== 'location' && f.type !== 'select' && f.type !== 'radio' && f.type !== 'checkbox') {
        ids.push(f.id);
      }
    }
    return ids;
  }, [formConfig]);

  const scrollToField = useCallback((fieldId: string) => {
    const y = fieldLayoutsRef.current[fieldId];
    if (y != null && scrollRef.current) {
      // Scroll so the field is ~100px from the top (comfortable reading position above keyboard)
      scrollRef.current.scrollTo({ y: Math.max(0, y - 100), animated: true });
    }
  }, []);

  const handleFieldFocus = useCallback((fieldId: string) => {
    const idx = textFieldIds.indexOf(fieldId);
    setFocusedIdx(idx);
    // Small delay to let keyboard animate open before scrolling
    setTimeout(() => scrollToField(fieldId), 150);
  }, [textFieldIds, scrollToField]);

  const handleFieldLayout = useCallback((fieldId: string, y: number) => {
    fieldLayoutsRef.current[fieldId] = y;
  }, []);

  const focusPrev = useCallback(() => {
    if (focusedIdx <= 0) return;
    const prevId = textFieldIds[focusedIdx - 1];
    const ref = inputRefsRef.current[prevId];
    if (ref) ref.focus();
  }, [focusedIdx, textFieldIds]);

  const focusNext = useCallback(() => {
    if (focusedIdx < 0 || focusedIdx >= textFieldIds.length - 1) return;
    const nextId = textFieldIds[focusedIdx + 1];
    const ref = inputRefsRef.current[nextId];
    if (ref) ref.focus();
  }, [focusedIdx, textFieldIds]);

  const dismissKeyboard = useCallback(() => {
    Keyboard.dismiss();
    setFocusedIdx(-1);
  }, []);

  // ── Phone duplicate detection ───────────────────────────────
  const [phoneDupError, setPhoneDupError] = useState<string | null>(null);
  const [checkingPhone, setCheckingPhone] = useState(false);
  const phoneCheckTimer = useRef<ReturnType<typeof setTimeout> | null>(null);

  // Detect which field is the phone field (resolved once when formConfig changes)
  const phoneFieldId = formConfig?.schema.fields.find((f) => {
    return f.type === 'phone'
      || matchesKeywords(f.label, PHONE_KEYWORDS)
      || matchesKeywords(f.id, PHONE_KEYWORDS);
  })?.id ?? null;

  // Run duplicate check whenever the phone value reaches 9 valid digits
  useEffect(() => {
    if (!phoneFieldId || !campaign?.id) return;

    const phoneValue = (formData[phoneFieldId] ?? '').trim();

    // Clear error + cancel pending check when phone is incomplete
    if (!PERU_PHONE_REGEX.test(phoneValue)) {
      setPhoneDupError(null);
      setCheckingPhone(false);
      if (phoneCheckTimer.current) clearTimeout(phoneCheckTimer.current);
      return;
    }

    // Debounce 300ms so we don't fire on every keystroke
    if (phoneCheckTimer.current) clearTimeout(phoneCheckTimer.current);

    phoneCheckTimer.current = setTimeout(async () => {
      setCheckingPhone(true);
      setPhoneDupError(null);

      try {
        // ── Check 1: SQLite local (immediate, offline-safe) ──────
        const localDup = await phoneExistsLocally(campaign.id, phoneValue);
        if (localDup) {
          setPhoneDupError('Este número ya fue registrado');
          setCheckingPhone(false);
          return;
        }

        // ── Check 2: Backend (requires network) ─────────────────
        const networkState = await Network.getNetworkStateAsync();
        if (networkState.isConnected && networkState.isInternetReachable) {
          const result = await checkPhoneDuplicate(phoneValue);
          if (result.ok && result.data.exists) {
            setPhoneDupError('Este número ya está registrado en el servidor');
            setCheckingPhone(false);
            return;
          }
        }

        // No duplicate found
        setPhoneDupError(null);
      } catch {
        // Network error → graceful degradation, let submission pass
        setPhoneDupError(null);
      } finally {
        setCheckingPhone(false);
      }
    }, 300);

    return () => {
      if (phoneCheckTimer.current) clearTimeout(phoneCheckTimer.current);
    };
  }, [formData, phoneFieldId, campaign?.id]);

  const updateField = useCallback((fieldId: string, value: string) => {
    setFormData((prev) => ({ ...prev, [fieldId]: value }));
  }, []);

  const handleLocationCaptured = useCallback((fieldId: string, value: string) => {
    setFormData((prev) => ({ ...prev, [fieldId]: value }));
    if (value) {
      try {
        setUbicacionUtm(JSON.parse(value));
      } catch {
        // ignore
      }
    }
  }, []);

  const handleSubmit = async () => {
    if (!formConfig) return;

    const fields = formConfig.schema.fields;
    
    // Validate required fields (skip optional ones like comentarios)
    for (const field of fields) {
      const isOptional = isOptionalField(field.id);
      
      // Skip validation for optional fields
      if (isOptional) continue;

      // Skip location fields here — they have their own GPS/distrito validation below (line 646)
      if (field.type === 'location') continue;
      
      if (field.required && !formData[field.id]?.trim()) {
        Alert.alert('Campo requerido', `"${field.label}" es obligatorio.`);
        return;
      }
      
      // Phone validation for Peru (9 digits starting with 9)
      const isPhoneField = field.type === 'phone' || matchesKeywords(field.label, PHONE_KEYWORDS) || matchesKeywords(field.id, PHONE_KEYWORDS);
      if (isPhoneField && formData[field.id]) {
        const phoneValue = formData[field.id].trim();
        if (!PERU_PHONE_REGEX.test(phoneValue)) {
          Alert.alert('Telefono invalido', 'El telefono debe tener 9 digitos y empezar con 9.');
          return;
        }
        // Block submission if duplicate was detected
        if (phoneDupError) {
          Alert.alert('Número duplicado', phoneDupError);
          return;
        }
      }
      
      if (field.validation?.pattern && formData[field.id]) {
        const regex = new RegExp(field.validation.pattern);
        if (!regex.test(formData[field.id])) {
          Alert.alert('Formato invalido', `"${field.label}" no tiene el formato correcto.`);
          return;
        }
      }
      if (field.validation?.min && formData[field.id]) {
        if (formData[field.id].length < field.validation.min) {
          Alert.alert('Muy corto', `"${field.label}" debe tener al menos ${field.validation.min} caracteres.`);
          return;
        }
      }
    }

    if (!ubicacionUtm && !gpsOpcional) {
      Alert.alert(
        'Ubicacion requerida',
        'Debes capturar la ubicacion GPS, o indicar el "Lugar de registro" si cargaste el dato desde casa.',
      );
      return;
    }

    setEnviando(true);

    try {
      // Map each form field to its backend column using type + label heuristics
      const resolved: Record<string, string> = {};

      for (const field of fields) {
        if (field.type === 'location') continue; // GPS handled separately
        const value = (formData[field.id] ?? '').trim();
        if (!value) continue;

        const backendKey = resolveBackendField(field);
        if (backendKey) {
          // First match wins (in case multiple fields resolve to same key)
          if (!resolved[backendKey]) {
            resolved[backendKey] = value;
          }
        }
      }

      // Generate unique client_id
      const clientId = generateClientId();

      // Build the full payload with resolved values (no fallbacks to placeholders)
      const payloadData: Record<string, unknown> = {
        nombre: resolved.nombre || 'Sin nombre',
        telefono: resolved.telefono || '',
        fecha: new Date().toISOString(),
        candidato_preferido: resolved.candidato_preferido || candidate.name,
        encuestador: agent.full_name,
        encuestador_id: agent.id,
        comentarios: resolved.comentarios || '',
      };

      if (ubicacionUtm) {
        // GPS capturado en campo
        payloadData.x = ubicacionUtm.easting;
        payloadData.y = ubicacionUtm.northing;
        payloadData.zona = `${ubicacionUtm.zone}${ubicacionUtm.hemisphere}`;
        payloadData.lat = ubicacionUtm.latitude;
        payloadData.lng = ubicacionUtm.longitude;
      } else if (lugarRegistro) {
        // Sin GPS — dato cargado desde casa, distrito seleccionado
        // x/y/zona son NOT NULL en DB: usar 0,0 y nombre del distrito como zona
        payloadData.x = 0;
        payloadData.y = 0;
        payloadData.zona = lugarRegistro.distrito;
        payloadData.lugar_registro = `${lugarRegistro.distrito}, ${lugarRegistro.provincia}, ${lugarRegistro.departamento}`;
        payloadData.ubigeo = lugarRegistro.ubigeo;
        payloadData.departamento = lugarRegistro.departamento;
        payloadData.provincia = lugarRegistro.provincia;
        payloadData.distrito = lugarRegistro.distrito;
        payloadData.coord_source = 'distrito_picker';
      }

      // OFFLINE-FIRST: Queue the form locally first
      await queueForm({
        client_id: clientId,
        campaign_id: campaign.id,
        form_definition_id: formConfig.id,
        data: payloadData,
      });

      // Notify dashboard immediately so the table updates
      appEvents.emit('forms:changed');

      // Try to sync immediately in background (non-blocking)
      forceSyncNow().catch(() => {
        // Ignore sync errors - data is safe in SQLite
      });

      // Reset form for next entry
      setFormData({});
      setUbicacionUtm(null);
      setLugarRegistro(null);

      // Success - form is saved locally and will sync when online
      Alert.alert(
        'Guardado',
        'Registro guardado. Se sincronizara automaticamente cuando haya conexion.',
        [{ text: 'OK', onPress: () => router.back() }]
      );
    } catch (err) {
      const message = err instanceof Error ? err.message : 'Error desconocido';
      Alert.alert('Error', `No se pudo guardar el formulario: ${message}`);
    } finally {
      setEnviando(false);
    }
  };

  // No form defined
  if (!formConfig) {
    return (
      <SafeAreaView style={styles.safeArea}>
        <View style={styles.noFormContainer}>
          <Text style={styles.noFormTitle}>Sin formulario</Text>
          <Text style={styles.noFormText}>
            No hay un formulario activo para esta campana.
          </Text>
          <Pressable
            style={[styles.backBtn, { backgroundColor: primary }]}
            onPress={() => router.back()}
          >
            <Text style={styles.backBtnText}>Volver</Text>
          </Pressable>
        </View>
      </SafeAreaView>
    );
  }

  const fields = formConfig.schema.fields;

  return (
    <SafeAreaView style={styles.safeArea}>
      <KeyboardAvoidingView
        style={styles.flex}
        behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
        keyboardVerticalOffset={Platform.OS === 'ios' ? 0 : 24}
      >
        <ScrollView
          ref={scrollRef}
          contentContainerStyle={styles.scroll}
          keyboardShouldPersistTaps="handled"
          keyboardDismissMode="interactive"
          showsVerticalScrollIndicator={false}
        >
          <View style={styles.header}>
            <Pressable
              onPress={() => router.back()}
              style={styles.backBtnSimple}
              accessibilityRole="button"
              accessibilityLabel="Volver a la pantalla anterior"
            >
              <Text style={[styles.backText, { color: TEXT_MUTED }]}>← Volver</Text>
            </Pressable>
            <Text style={[styles.title, { color: primary }]} accessibilityRole="header">
              {formConfig.name}
            </Text>
            <Text style={[styles.subtitle, { color: TEXT_MUTED }]}>Agente: {agent.full_name}</Text>
          </View>

          <View style={styles.form}>
            {fields.map((field) => (
              <DynamicField
                key={field.id}
                field={field}
                value={formData[field.id] ?? ''}
                onChange={
                  field.type === 'location'
                    ? (val) => handleLocationCaptured(field.id, val)
                    : (val) => updateField(field.id, val)
                }
                primaryColor={primary}
                externalError={field.id === phoneFieldId ? phoneDupError : null}
                checkingPhone={field.id === phoneFieldId ? checkingPhone : false}
                gpsOpcional={field.type === 'location' ? gpsOpcional : undefined}
                onFocus={() => handleFieldFocus(field.id)}
                onLayout={(e) => handleFieldLayout(field.id, e.nativeEvent.layout.y)}
                inputAccessoryViewID={ACCESSORY_ID}
                inputRef={(r) => { inputRefsRef.current[field.id] = r; }}
              />
            ))}

            <View style={[styles.candidatoCard, { backgroundColor: primary }]}>
              <Text style={styles.candidatoLabel}>Candidato</Text>
              <Text style={styles.candidatoName}>{candidate.name}</Text>
              <Text style={[styles.candidatoDetail, { color: secondary }]}>
                {candidate.cargo} · {candidate.partido}
              </Text>
            </View>

            {/* ── Lugar de registro (hace GPS opcional) ─────────── */}
            <View
              style={styles.lugarRegistroCard}
              onLayout={(e) => handleFieldLayout('__lugar_registro__', e.nativeEvent.layout.y)}
            >
              <Text style={styles.lugarRegistroTitle}>
                ¿Dato tomado en papel?
              </Text>
              <Text style={styles.lugarRegistroDesc}>
                Si cargaste este registro desde casa, selecciona el distrito
                donde recogiste el dato. Esto reemplaza la ubicacion GPS.
              </Text>
              <DistritoPicker
                value={lugarRegistro}
                onSelect={setLugarRegistro}
                onClear={() => setLugarRegistro(null)}
                primaryColor={primary}
                placeholder="Seleccionar distrito..."
              />
              {lugarRegistro && (
                <Text style={styles.lugarRegistroHint}>
                  GPS omitido — registrado en {lugarRegistro.distrito}, {lugarRegistro.provincia}.
                </Text>
              )}
            </View>
          </View>

          <Pressable
            style={[styles.saveBtn, { backgroundColor: secondary }, (enviando || !!phoneDupError || checkingPhone) && styles.saveBtnDisabled]}
            onPress={() => { dismissKeyboard(); handleSubmit(); }}
            disabled={enviando || !!phoneDupError || checkingPhone}
            accessibilityRole="button"
            accessibilityLabel={enviando ? 'Enviando registro' : 'Enviar registro'}
          >
            <Text style={[styles.saveBtnText, { color: primary }]}>
              {enviando ? 'Enviando...' : checkingPhone ? 'Verificando...' : 'Enviar registro'}
            </Text>
          </Pressable>

          {/* Extra space at bottom so last field isn't stuck against the keyboard */}
          <View style={styles.keyboardSpacer} />
        </ScrollView>
      </KeyboardAvoidingView>

      {/* iOS keyboard toolbar with Prev / Next / Done */}
      <KeyboardToolbar
        onPrev={focusPrev}
        onNext={focusNext}
        onDone={dismissKeyboard}
        hasPrev={focusedIdx > 0}
        hasNext={focusedIdx >= 0 && focusedIdx < textFieldIds.length - 1}
        primaryColor={primary}
      />
    </SafeAreaView>
  );
}


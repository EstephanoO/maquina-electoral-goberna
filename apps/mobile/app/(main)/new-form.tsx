// Peru phone validation: 9 digits starting with 9
const PERU_PHONE_REGEX = /^9\d{8}$/;

// Fields that are explicitly optional (no asterisk, no validation)
const OPTIONAL_FIELDS = ['comentarios', 'observaciones', 'notas'];

// ─── Resolve form fields to backend flat schema ──────────────
// The backend expects: nombre, telefono, zona, comentarios, candidato_preferido.
// Form definitions may have arbitrary field IDs (e.g. "field_1739812345678")
// generated by the web editor. We resolve using field type + label keywords.

const NOMBRE_KEYWORDS = ['nombre', 'apellido', 'name'];
const PHONE_KEYWORDS = ['telefono', 'celular', 'phone', 'movil'];
const ZONA_KEYWORDS = ['ubicacion', 'zona', 'sector', 'distrito', 'direccion', 'barrio', 'localidad'];
const COMMENT_KEYWORDS = ['comentario', 'observacion', 'nota', 'detalle'];
const CANDIDATE_KEYWORDS = ['preferido', 'candidato', 'voto'];

function matchesKeywords(text: string, keywords: string[]): boolean {
  const normalized = text.toLowerCase().replace(/[^a-z]/g, '');
  return keywords.some((kw) => normalized.includes(kw));
}

/**
 * Given a form field, determine which backend column it maps to.
 * Priority: field.type > label keywords > id keywords > null (unknown)
 */
function resolveBackendField(field: FormField): string | null {
  const { id, type, label } = field;

  // 1. Type-based resolution (most reliable)
  if (type === 'phone') return 'telefono';
  if (type === 'location') return null; // handled separately via UTM

  // 2. Label-based resolution (works for custom field IDs)
  if (matchesKeywords(label, NOMBRE_KEYWORDS)) return 'nombre';
  if (matchesKeywords(label, PHONE_KEYWORDS)) return 'telefono';
  if (matchesKeywords(label, ZONA_KEYWORDS)) return 'zona';
  if (matchesKeywords(label, COMMENT_KEYWORDS)) return 'comentarios';
  if (matchesKeywords(label, CANDIDATE_KEYWORDS)) return 'candidato_preferido';

  // 3. ID-based resolution (fallback for well-known IDs)
  if (matchesKeywords(id, NOMBRE_KEYWORDS)) return 'nombre';
  if (matchesKeywords(id, PHONE_KEYWORDS)) return 'telefono';
  if (matchesKeywords(id, ZONA_KEYWORDS)) return 'zona';
  if (matchesKeywords(id, COMMENT_KEYWORDS)) return 'comentarios';
  if (matchesKeywords(id, CANDIDATE_KEYWORDS)) return 'candidato_preferido';

  // 4. Unknown field — will be included as-is for the backend to ignore
  return null;
}


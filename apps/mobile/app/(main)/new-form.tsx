// Check if a field is optional based on its ID
function isOptionalField(fieldId: string): boolean {
  const normalized = fieldId.toLowerCase().replace(/[^a-z]/g, '');
  return OPTIONAL_FIELDS.some((opt) => normalized.includes(opt));
}

// Render the required asterisk in red
function RequiredAsterisk() {
  return <Text style={styles.requiredAsterisk}>*</Text>;
}

function DynamicField({
  field,
  value,
  onChange,
  primaryColor,
  externalError,
  checkingPhone,
  gpsOpcional,
  onFocus,
  onLayout,
  inputAccessoryViewID,
  inputRef,
}: {
  field: FormField;
  value: string;
  onChange: (val: string) => void;
  primaryColor: string;
  /** Error message injected by the parent (e.g. duplicate phone) */
  externalError?: string | null;
  /** Whether a phone duplicate check is in progress */
  checkingPhone?: boolean;
  /** If true, GPS field shows as optional (lugar_registro was filled) */
  gpsOpcional?: boolean;
  /** Called when the field's text input gains focus */
  onFocus?: () => void;
  /** Capture layout for scroll-to-focused */
  onLayout?: (event: { nativeEvent: { layout: { y: number } } }) => void;
  /** InputAccessoryView ID for iOS keyboard toolbar */
  inputAccessoryViewID?: string;
  /** Ref callback for the underlying TextInput */
  inputRef?: (ref: TextInput | null) => void;
}) {
  const [capturandoGps, setCapturandoGps] = useState(false);
  const [phoneError, setPhoneError] = useState<string | null>(null);

  // Determine if this field should show required asterisk
  const isOptional = isOptionalField(field.id);
  const showRequired = field.required && !isOptional;

  // Handle phone input with validation
  const handlePhoneChange = (text: string) => {
    // Only allow digits
    const cleaned = text.replace(/\D/g, '');
    // Limit to 9 digits
    const limited = cleaned.slice(0, 9);
    onChange(limited);

    // Validate
    if (limited.length > 0 && limited.length < 9) {
      setPhoneError('Debe tener 9 digitos');
    } else if (limited.length === 9 && !limited.startsWith('9')) {
      setPhoneError('Debe empezar con 9');
    } else {
      setPhoneError(null);
    }
  };

  if (field.type === 'location') {
    const hasLocation = value !== '';
    // Si gpsOpcional=true (lugar_registro lleno), el campo GPS no muestra asterisco
    const showRequiredGps = field.required && !gpsOpcional;

    const captureLocation = async () => {
      if (capturandoGps) return;
      setCapturandoGps(true);
      try {
        // Check first — only request if not already granted.
        // Apple 5.1.1: the system dialog must come from a user action (this is a tap handler, so compliant).
        const existing = await Location.getForegroundPermissionsAsync();
        let permStatus = existing.status;
        if (permStatus !== 'granted') {
          const { status } = await Location.requestForegroundPermissionsAsync();
          permStatus = status;
        }
        if (permStatus !== 'granted') {
          Alert.alert(
            'Permiso denegado',
            'Se necesita acceso a la ubicacion para registrar coordenadas GPS.',
          );
          return;
        }
        const position = await Location.getCurrentPositionAsync({
          accuracy: Location.Accuracy.High,
        });
        const { latitude, longitude } = position.coords;
        const utm = latLonToUtm(latitude, longitude);
        onChange(JSON.stringify(utm));
      } catch (err) {
        const message = err instanceof Error ? err.message : 'Error desconocido';
        Alert.alert('Error GPS', `No se pudo obtener la ubicacion: ${message}`);
      } finally {
        setCapturandoGps(false);
      }
    };

    let displayText = capturandoGps ? 'Obteniendo GPS...' : 'Ingresar ubicacion';
    if (hasLocation) {
      try {
        const utm: UtmData = JSON.parse(value);
        displayText = `UTM ${utm.zone}${utm.hemisphere} ${Math.round(utm.easting)}E ${Math.round(utm.northing)}N`;
      } catch {
        displayText = 'Ubicacion registrada';
      }
    }

    return (
      <View style={styles.field} onLayout={onLayout}>
        <Text style={styles.label}>
          {field.label}{' '}
          {showRequiredGps ? <RequiredAsterisk /> : (
            gpsOpcional ? <Text style={styles.optionalLabel}> (opcional)</Text> : null
          )}
        </Text>
        <Pressable
          style={[
            styles.gpsBtn,
            { borderColor: primaryColor },
            hasLocation && styles.gpsBtnCaptured,
            gpsOpcional && !hasLocation && styles.gpsBtnOptional,
            capturandoGps && { opacity: 0.6 },
          ]}
          onPress={captureLocation}
          disabled={capturandoGps}
          accessibilityRole="button"
          accessibilityLabel={hasLocation ? `GPS capturado: ${displayText}` : 'Capturar ubicacion GPS'}
        >
          <Text style={[
            styles.gpsBtnText,
            { color: primaryColor },
            hasLocation && styles.gpsBtnTextCaptured,
            gpsOpcional && !hasLocation && styles.gpsBtnTextOptional,
          ]}>
            {gpsOpcional && !hasLocation ? 'Agregar GPS (opcional)' : displayText}
          </Text>
        </Pressable>
        {gpsOpcional && !hasLocation && (
          <Text style={styles.gpsOpcionalHint}>
            Omitido — usaras el lugar indicado abajo.
          </Text>
        )}
      </View>
    );
  }

  if (field.type === 'select' || field.type === 'radio') {
    return (
      <View style={styles.field} onLayout={onLayout}>
        <Text style={styles.label}>
          {field.label} {showRequired && <RequiredAsterisk />}
        </Text>
        <View style={styles.optionsContainer}>
          {(field.options ?? []).map((opt) => (
            <Pressable
              key={opt.value}
              style={[
                styles.optionBtn,
                value === opt.value && { backgroundColor: primaryColor, borderColor: primaryColor },
              ]}
              onPress={() => onChange(opt.value)}
              accessibilityRole="radio"
              accessibilityState={{ checked: value === opt.value }}
              accessibilityLabel={opt.label}
            >
              <Text
                style={[
                  styles.optionText,
                  value === opt.value && { color: '#FFFFFF' },
                ]}
              >
                {opt.label}
              </Text>
            </Pressable>
          ))}
        </View>
      </View>
    );
  }

  if (field.type === 'checkbox') {
    const checked = value === 'true';
    return (
      <View style={styles.field} onLayout={onLayout}>
        <Pressable
          style={styles.checkboxRow}
          onPress={() => onChange(checked ? 'false' : 'true')}
          accessibilityRole="checkbox"
          accessibilityState={{ checked }}
          accessibilityLabel={field.label}
        >
          <View style={[styles.checkbox, checked && { backgroundColor: primaryColor, borderColor: primaryColor }]}>
            {checked && <Text style={styles.checkmark}>✓</Text>}
          </View>
          <Text style={styles.checkboxLabel}>{field.label}</Text>
        </Pressable>
      </View>
    );
  }

  // Handle phone fields specially (by type or label/id keywords)
  const isPhoneField = field.type === 'phone' || matchesKeywords(field.label, PHONE_KEYWORDS) || matchesKeywords(field.id, PHONE_KEYWORDS);

  const keyboardType =
    field.type === 'phone' || isPhoneField
      ? 'phone-pad' as const
      : field.type === 'number'
      ? 'numeric' as const
      : field.type === 'email'
      ? 'email-address' as const
      : 'default' as const;

  const multiline = field.type === 'textarea';

  // Combined error: local format error takes priority, then external (duplicate) error
  const displayError = isPhoneField ? (phoneError ?? externalError ?? null) : null;
  const hasError = displayError !== null;

  return (
    <View style={styles.field} onLayout={onLayout}>
      <Text style={styles.label}>
        {field.label} {showRequired && <RequiredAsterisk />}
        {isOptional && <Text style={styles.optionalLabel}> (opcional)</Text>}
      </Text>
      <View>
        <TextInput
          ref={(r) => inputRef?.(r)}
          style={[
            styles.input,
            multiline && styles.inputMultiline,
            isPhoneField && hasError && styles.inputError,
          ]}
          placeholder={isPhoneField ? '987654321' : (field.placeholder ?? '')}
          placeholderTextColor={TEXT_MUTED}
          value={value}
          onChangeText={isPhoneField ? handlePhoneChange : onChange}
          onFocus={onFocus}
          keyboardType={keyboardType}
          multiline={multiline}
          numberOfLines={multiline ? 3 : 1}
          textAlignVertical={multiline ? 'top' : 'auto'}
          maxLength={isPhoneField ? 9 : field.validation?.maxLength}
          autoCapitalize={field.type === 'email' ? 'none' : 'sentences'}
          inputAccessoryViewID={inputAccessoryViewID}
        />
        {isPhoneField && checkingPhone && (
          <View style={styles.phoneCheckingIndicator}>
            <ActivityIndicator size="small" color={TEXT_MUTED} />
          </View>
        )}
      </View>
      {isPhoneField && hasError && (
        <Text style={styles.fieldError}>{displayError}</Text>
      )}
    </View>
  );
}

